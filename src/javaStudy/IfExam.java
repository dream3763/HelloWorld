package javaStudy;

public class IfExam {

	public static void main(String[] args) {
		int x = 50;
		int y = 60;
		
		if(x < y) {
			System.out.println("x는 y 보다 작다");
		}else if(x > y) {
			System.out.println("x는 y 보다 크다");
		}else {
			System.out.println("x와 y 는 같다");
		}
			

	}

}
